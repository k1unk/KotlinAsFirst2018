fun lineBySegment(s: Segment): Line = lineByPoints(s.begin, s.end)

/**
 * Средняя
 *
 * Построить прямую по двум точкам
 */
fun lineByPoints(a: Point, b: Point): Line {
    var angle = atan2(a.y - b.y, a.x - b.x)
    if (angle < 0) while (angle < 0) angle += PI
    if (angle >= PI) while (angle >= PI) angle -= PI
    return Line(a, angle)
}

/**
 * Сложная
 *
 * Построить серединный перпендикуляр по отрезку или по двум точкам
 */
fun bisectorByPoints(a: Point, b: Point): Line {
    val x1 = a.x                                                     // координаты
    val y1 = a.y                                                     // точки a

    val x2 = b.x                                                     // координаты
    val y2 = b.y                                                     // точки b

    val center = Point((x1 + x2) / 2, (y1 + y2) / 2)           // середина отрезка

    var angle = atan2(y2 - y1, x2 - x1) + PI / 2               // угол
    if (angle < 0) while (angle < 0) angle += PI                     //
    if (angle >= PI) while (angle >= PI) angle -= PI                 //

    return Line(center, angle)                                       // серединный перпендикуляр
}

/**
 * Средняя
 *
 * Задан список из n окружностей на плоскости. Найти пару наименее удалённых из них.
 * Если в списке менее двух окружностей, бросить IllegalArgumentException
 */
fun findNearestCirclePair(vararg circles: Circle): Pair<Circle, Circle> {
    if (circles.size < 2) throw IllegalArgumentException()
    var min = circles[0].distance(circles[1])
    var pairMin = Pair(circles[0], circles[1])
    for (i in 0 until circles.size) {
        for (j in i + 1 until circles.size) {
            if (circles[i].distance(circles[j]) < min) {
                min = circles[i].distance(circles[j])
                pairMin = Pair(circles[i], circles[j])
            }
        }
    }
    return pairMin
}

